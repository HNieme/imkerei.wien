						<h2>Zusammenbauen</h2>
						<p>
							<b>Einkauf der Holzbauteile:</b> In Baumärkten gibt es häufig die Möglichkeit, sich Holz(platten) nach Plan zuschneiden zu lassen. Dadurch erhält man praktisch einen Bausatz für Beuten, nur viel günstiger. Je nach Holzqualität und Baumarkt gibt es große Preisunterschiede. Ich habe im Mai 2018 8,49€ für eine Platte 2000*400*18mm (Naturwuchs) im Bauhaus bezahlt. Bei Obi waren diese wesentlich teurer.
							<b>Allgemeiner Hinweis bezüglich verschrauben:</b> <br />
							Die Löcher für die Schrauben sollten vorgebohrt werden. Ich habe dafür einen Bohrer verwendet, der etwas dünner als der Stab der Schraube (ohne Gewinde) war. 
							<br /><br />
							Für die Schraubenköpfe sollte das Holz angesenkt werden, damit sie nachher nicht herausstehen. Das kann mit einem gewöhnlichen Bohrer (Durchmesser so groß wie Schraubenkopf), mit Ansenkern oder mit Ansenkern, die direkt am Bohrer montiert sind (Siehe Bild unten), geschehen. 
