					<h3 id="29.06"><a href="#29.06" >Varroabehandlung</a></h3>
					<p>
					    <br /><br />
					    Seit in den 70ern die Varroamilbe in Österreich eingeschleppt wurde, müssen wir Imker unsere
					    Bienen regelmäßig gegen diese Parasiten behandeln.
					    <br /><br />
					    Die meisten Imker behandeln zwei Mal im Sommer, indem sie Ameisensäure in speziellen Verdunstern
					    verdunsten lassen. Ameisensäure wird in der Natur beispielsweise von Brennnesseln und Ameisen
					    zur Verteidigung verwendet, selbst in Honig kommt sie in sehr geringen Mengen natürlich vor.
					    In konzentrierter Form ist Ameisensäure aber ziemlich ätzend (man denke daran, wie unangenehm
					    Brennnesseln brennen!). Daher verwenden wir bei der Behandlung Schutzbrillen und säureresistente
					    Handschuhe.
					    <br /><br />
					    Früher wurden Akarizide (=Spinnengifte) zur Behandlung der Bienen verwendet. Leider entwickelten
					    die Milben bald Resistenzen, sodass immer neue Substanzen gefunden werden mussten. Außerdem
					    kontaminierten diese Mittel auch die Waben und damit das Bienenwachs, da sie fettlöslich sind.
					    Noch heute sind diese Substanzen in vielen Bienenwachsen vorhanden. Daher kaufte ich immer nur
					    zertifiziert rückstandsfreies Wachs und lasse mein eigenes Bienenwachs wieder zu neuen
					    Wabenplatten verarbeiten. So gewährleiste ich eine ausgezeichnete Qualität meiner Bienenprodukte.
					    <br /><br />
					    Auch in anderen Bereichen der Betriebsführung steht dies immer an erster Stelle:<br />
					    Um die Qualität des Honigs keinesfalls zu beeinträchtigen achten wir darauf die Behandlung erst
					    durchzuführen, nachdem der gesamte Honig geerntet wurde. So ist sichergestellt, dass ein
					    hochwertiges Produkt von gesunden Bienen im Glas landet.
					    <br /><br />
					    Über die dritte Behandlung im Winter berichte ich dann im Dezember!

